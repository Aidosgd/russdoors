<h1>ДверЛайн</h1>
<p><img style="float: left; margin-top: 2px; margin-bottom: 2px; margin-left: 5px; margin-right: 5px;" src="assets/images/comp-logo/dverline.jpg" alt="" width="140" height="121" /></p>
<p>Фабрика по производству межкомнатных дверей &laquo;ДверЛайн&raquo; - современное, динамично развивающее предприятие, выпускающее широкий ассортимент межкомнатных дверей от классических до современных. Межкомнатные двери один из ключевых элементов интерьера. Дизайнер, пользуясь продукцией нашей фабрики может реализовать любой из задуманных проектов, выбирая цветовое решение на собственное усмотрение.</p>
<p>Межкомнатные двери являются экологически чистым изделием, поскольку основа дверного полотна изготовлена из массива дерева, а направляющие изготовлены из бруса ЛВЛ. Все использованные материалы являются экологически чистыми и на каждый из них имеются сертификаты. Наша фабрика изготавливает межкомнатные двери из материала, прошедшего</p>
<p>сушку в оптимальном технологическом режиме. Влажность изделий, выходящих с фабрики, составляет порядка 9%.</p>
<p>Все межкомнатные двери изготовлены на современном высокотехнологичном оборудовании производства Италии и Германии. Внутреннее заполнение дверных полотен производится высококачественным сотовым наполнителем. Каркас дверного полотна облицовывается МДФ с натуральным шпоном или ламинированное искусственным шпоном.</p>
<p>Представленные дверные полотна изготовлены с использованием ламинированного багета, раскладки, а также комплектуются дверной коробкой и наличником, облицованные соответствующими покрытиями. Остеклённые дверные полотна комплектуются узорчатым стеклом или художественным стеклом с элементами фьюзинга. Художественное стекло представляет собой отечественное полированное стекло толщиной 4- 5 мм, на котором выполнен рисунок методом пескоструйной гравировки. Художественное стекло изготавливается на собственной технологической линии, что позволяет оперативно внедрять новые дизайнерские решения.</p>
