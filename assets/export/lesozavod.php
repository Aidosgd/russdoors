<h1>Лесозавод</h1>
<p><img style="float: left; margin-top: 2px; margin-bottom: 2px; margin-left: 7px; margin-right: 7px;" src="assets/images/comp-logo/lesozavod.jpg" alt="" width="295" height="174" /></p>
<p>Закрытое акционерное общество &laquo;Производственное объединение &laquo;ЛЕСОЗАВОД&raquo;</p>
<p>Производственное объединение Лесозавод создано на базе акционерного общества &laquo;Ржевмебель&raquo; в 2001г. и располагается в г. Ржев Тверской области на территории 17га.</p>
<p>Объединение занимается производством межкомнатных дверей, строганных заготовок, окутанного и строганного погонажа, мебельного щита, каркасных домов по технологии и на оборудовании канадской фирмы MiTek.</p>
<p>Предприятие оснащено современным оборудованием . В арсенале предприятия сушильный комплекс фирмы BASCHILD на 600м3 разовой загрузки, строгальные станки, обрабатывающие центры, шлифовальные и кромкооблицовочные станки, оборудование для окутывания погонажа фирм IMA, HOMAG, FRIZ, MAKOR, ERNST и т.д.</p>
<p>На предприятии работает более 400 человек.</p>
<p>Благодаря постоянному совершенствованию технологий предприятие является одним из крупных игроков на Российском, а также Казахстанском рынке деревообработки.</p>
<p>В 2007 году был запущен и успешно реализуется проект &laquo;LEGODOM&raquo; - каркасное домостроение с применением технологии &laquo;MiTek&raquo;. Полный цикл деревообработки, наличие конструкторского отдела и необходимая производственная и строительная базы позволяют воплотить идеи заказчика в архитектурное и инженерное решение с последующей реализацией проектов каркасных домов &laquo;под ключ&raquo;.</p>
