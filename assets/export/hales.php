<h1>Халес</h1>
<p><img style="float: left; margin-top: 2px; margin-bottom: 2px; margin-left: 5px; margin-right: 5px;" src="assets/images/comp-logo/hales.jpg" alt="" width="220" height="96" />Деятельность нашей компании началась в октябре 1999 г. с выпуска дверей модели &laquo;Капри-1&raquo;, которая до сих пор остается в производственной программе. За 14 лет непрерывной работы и совершенствования технологии производства межкомнатных дверей мы стали лидерами в Беларуси по их выпуску. Сегодня компания &laquo;Халес&raquo; поставляет дверные блоки в Россию, Украину, Казахстан, Италию, Словакию, Чехию, Латвию, Литву, Молдову и Татарстан. Собственный сушильный комплекс, автоматизированное высокопроизводительное оборудование, постоянный контроль и квалифицированный персонал позволяют выпускать продукцию со стабильным качеством, в срок и в нужном объеме.</p>
<p>На текущий момент производственная программа включает в себя более 18 моделей дверных полотен. Большое разнообразие отделки различными видами шпона и покраски дополнены новыми видами отделки с элементами искусственного старения, патинирования, имитация золочения, различными вариантами стекол. На любую модель мы можем предложить несколько вариантов коробок собственного производства: простые, телескопические, с уплотнителем, наличником, доборной доской, декоративными элементами.</p>
<p>&nbsp;</p>
<p>Постоянное наличие на складе самых ходовых моделей позволяет нам в кратчайшие сроки исполнить практически любой заказ. Вся наша продукция имеет сертификаты соответствия и гигиенические удостоверения. Предлагая продукцию с выгодным соотношением цена/качество, мы заинтересованы в сотрудничестве с компаниями, занимающимися строительством и торговлей во всех регионах, куда можно попасть с помощью железнодорожного, автомобильного или морского транспорта. Большой опыт наших таможенных агентов позволяет учесть все пожелания покупателей в разных странах.</p>
<p>В последние годы согласно существующим мировым трендам компания начала выпуск серии новых моделей под брэндом &laquo;Renaissance&raquo;. &laquo;Renaissance&raquo; - это всё лучшее из классики. Выверенные формы, разные варианты отделки, возможность комплектации различными наличниками, цоколями и капителями позволяет создать настоящее произведение искусства.</p>
