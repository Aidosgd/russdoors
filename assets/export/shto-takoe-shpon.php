<h1>Что такое шпон</h1>
<p><strong><br />Шпон - это тонкий слой древесины определенной толщины, произведенный лущением, строганием, или распиливанием бревен.
</strong></p>
<p><strong><img style="float: left; margin-top: 5px; margin-bottom: 5px; margin-left: 10px; margin-right: 10px;" title="история производства шпона" src="assets/images/articles/1/art_1.jpg" alt="что такое шпон - российские двери" width="183" height="129" /></strong>Технология производства шпона известна уже более 4000 лет. Не случайно, что идея распиливать дерево на тонкие листы возникла в высокоразвитой культуре Древнего Египта - стране, в которой кроме живительной воды Нила, была только пустыня. Дерево в Древнем Египте было редкостью и ценилось так же, как драгоценные камни, которые применялись для украшения мебели.</p>
<p>И так, производство шпона возникло не там, где густые леса покрывают ландшафт, а там, где древесина была дефицитным сырьём и существовала необходимость его оптимального использования. Кустарное производство, при котором шпон нарезался со ствола поперечной пилой, было делом долгим и трудным. Тем не менее, прекрасные деревянные изделия, найденные в могиле Тутанхамона, ясно показывают, что, несмотря на примитивные методы обработки, люди того времени уже знали, как показать естественную внутреннюю красоту древесины.
</p>
<p>Мебель, достигшая художественного совершенства в периоды ренессанса и барокко, а также созданная в последующие столетия великими мастерами, демонстрирует, как простая потребность может стать культурным наследием и, в конечном счете, развиться в отличительную черту каждого исторического периода.
</p>
<p><img style="float: left; margin-top: 5px; margin-bottom: 5px; margin-left: 10px; margin-right: 10px;" title="двери шпон в алматы" src="assets/images/articles/1/art_2.jpg" alt="история шпонированных дверей" width="182" height="126" />В древних замках наш взгляд скользит по художественно выполненным деревянным покрытиям (часто в виде инкрустаций), по сохранившейся мебели и внутреннему убранству. В наши дни это завещание мастеров ушедших культур, утончённое и усовершенствованное современной техникой, нашло своё применение в промышленном производстве.</p>
<p>Механизировать методы производства шпона удалось лишь в 19 веке. В 1806 году Марк Исамбард Брунель получил британский патент на строгальный станок с ручным приводом, а Генри Фаверер, тоже англичанин, в 1818 году изобрёл лущильный станок для производства шпона. В 1843 году появилась первая фабрика в Германии, оборудованная в то время ещё обычными пилами. В США строганый шпон был произведен в начале 19 века на станке шириной около 1,5 м. В 1870 г. строганый шпон начали производить во Франции. А в 1901 г. в Италии семья Кремона освоила производство строганого шпона на первом горизонтальном строгальном станке. </p>
<p>
И всё-таки производство шпона всегда было профессией. Профессиональное мастерство и опыт здесь играют решающую роль. Высокий профессиональный уровень требуется уже при определении и оценке качества необработанного лесоматериала, при решении вопросов о возможностях переработки с целью получения наилучшего рисунка, и, разумеется, необходим, для правильной оценки готовой продукции, чтобы определить наилучший способ её применения. </p>
<p>
При производстве строганого шпона бегающий нож строгает предварительно закрепленное бревно (в то время как при получении лущеного шпона бревно вращается вокруг неподвижного ножа). Таким образом, получают листы шпона толщиной от 0,2 до 3 мм, которые складывают в пачки ("кноли"). Определенное количество пачек формирует пакет ("паллету"). </p>
