<h1>Как установить межкомнатные двери	</h1>
<p>Продолжение статьи <strong><a href="ustanovka-dverei.php">Правильная установка входных и межкомнатных дверей:
основные этапы и способы</a></strong>
&nbsp;<br /><strong><br /><br />Первый способ</strong></p>
<p>Установка сквозным методом. 
По дверному полотну собирается дверная коробка, врезаются две разъёмные петли и замок. Дверной блок устанавливается в проём с помощью 6-8 саморезов, которые крепят просверленную на сквозь коробку к стене. Дверной блок выставляется по месту с помощью деревянных клиньев, таким образом, что бы дверь в любом из открытых состояний стояла на месте. После чего зазор между дверной коробкой и стеной проливается монтажной пеной.</p>
<p>
<strong>Второй способ</strong></p>
<p>Установка двери на пену.  
По дверному полотну собирается дверная коробка. С помощью рулетки, уровня, закладных, самой двери и деревянных клиньев, выставляется в проёме. Проливается монтажной пеной. После полного высыхания пены (24 часа) врезаются петли, на дверную коробку, а по ним уже на саму дверь. Регулировка зазора по притвору выставляется, глубиной врезки петель. Врезается замок. Монтажная пена имеет свойство расширяться-сужаться, в зависимости от влажности. Как следствие, коробка начнет деформироваться, а дверь попросту перестанет закрываться.</p>
<p>
<strong>Третий способ</strong></p>
<p>По дверному полотну собирается дверная коробка, врезаются две разъёмные петли и замок. Дверной блок устанавливается в проём, с помощью 3-6 точек крепления сквозным методом под петли и ответную планку замка. Дверной блок выставляется по месту с помощью деревянных клиньев и проливается монтажной пеной. Недостатком этого способа является ненадёжность крепления только в трёх точках.
</p>
<p><strong>Четвёртый способ</strong></p>
<p>По дверному полотну собирается дверная коробка, врезаются две разъёмные петли и замок. Дверной блок устанавливается в проём, с помощью деревянных клиньев и монтажной ленты, которая с двух сторон, с помощью саморезов крепится к торцам дверной коробки и стене. Проливается монтажной пеной. 
Самый большой недостаток этого способа, это невозможность точно выставить дверной блок, поскольку саморезы при креплении заходят в стену под 45 градусов.</p>
<p>
Первый способ установки более удобен, поскольку именно в этом варианте дверная коробка, надёжнее фиксируется в проёме. Так же, если установить межкомнатные двери этим способом, времени уходит меньше, чем на все остальные.</p>
