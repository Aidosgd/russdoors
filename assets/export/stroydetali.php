Компания<strong> ТОО "Российские Двери" Алматы</strong> предлагает межкомнатные двери производства АО &laquo;Стройдетали&raquo; Белларуси.</p>
<p>
ОАО &laquo;Стройдетали&raquo; создано в 1994 году на базе государственного предприятия &laquo;Вилейский завод строительных деталей&raquo; путем разгосударствления и приватизации. Имеет более чем вековую историю, с момента создания в 1905 году на этой территории лесопильного производства с паросиловым приводом.</p>
<p>С 1993 года на предприятии приступили к решительному реформированию производства, основой являлась стратегия, выработанная на основе глубокого анализа мирового опыта &ndash; сужение специализации, направленное на достижение совершенства в избранной сфере деятельности.</p>

