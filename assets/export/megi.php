<h1>Входные металлические стальные двери МЕГИ в Алматы</h1>
<p><img style="float: left; margin: 0px 5px;" src="assets/images/comp-logo/MEGI_logo.png" alt="" width="200" height="96" />Группа предприятий МЕГИ была создана в 1995 году и в настоящее время включает в себя ООО "Медстальконструкция" - завод выпускающий широкий ассортимент мебельной продукции, в том числе медицинскую и лабораторную мебель, металлическую офисную мебель, стеллажи, мебель из ЛДСП, и торгово-сервисные фирмы, реализующие указанную продукцию и предоставляющие услуги по доставке, сборке и гарантийному обслужванию:</p>
<p>* ООО "МЕГИ" - мебельная продукция</p>
<p>* ООО "Стальные двери МЕГИ" - дверная продукция</p>
<p>* ООО "ТД МЕГИ" - экспорт продукции</p>
<p>В 2009 году ГП МЕГИ разработаны входные стальные двери и начат серийный выпуск дверей эконом-класса. Основными рыночными идеями новой дверной продукции, выпускаемой нами под брендомСТАЛЬНЫЕ ДВЕРИ МЕГИ стали:</p>
<p>* высокая технологичность, обеспечивающая низкую себестоимость производства стальных дверей</p>
<p>* выская надежность, за счет использования отечественных материалов и комплектующих</p>
<p>Наш завод оснащён большим современным станочным парком, обладающим широкими возможностями и уникальными техническими характеристиками, и позволяющим исполнять как системные заказы с заданными графиками отгрузки крупных партий продукции, так и единичные заказы, содержащие ассортимент в несколько сотен наименований серийной продукции.</p>
<p>Снижение себестоимости выпускаемой продукции достигается благодаря высокой точности и быстродействию оборудования, высокой степени автоматизации производства и многолетнему опыту работы в области металлообработки.</p>
<p>ГП МЕГИ выполняет также полный комплекс услуг по листообработке металла на условиях промышленного аутсорсинга и может предложить конкурентноспособные цены на свои услуги.</p>
<p>Листообработка металла выполняется на нашем предприятии с использованием следующего оборудования:</p>
<p>* Координатная пробивка и перфорация металла - комбинированный пробивной пресс с угловыми ножницами Finn-Power SG6, координатно-пробивной пресс Finn-Power C5 * Гибка - листогибы Finn-Power *Изготовление панелей - панелегибочная машина Salvagnini * Лазерная резка - станок TruLaser 3030 NEW</p>
