<h1>Поставский Мебельный Центр</h1>
<p><img style="margin: 5px 10px; float: left;" src="assets/images/comp-logo/enter_logo.png" alt="" width="196" height="200" /></p>
<p>ЧПУП &laquo;ПОСТАВСКИЙ МЕБЕЛЬНЫЙ ЦЕНТР&raquo; (ПМЦ) на протяжении более 10 лет занимается производством дверей. Располагается предприятие в живописном уголке Беларуси в г. Поставы. В краю с богатыми лесами и озерами. Граничит с Браславским заповедником, курортным поселком Нарочь, а также с Литвой.</p>
<p>На протяжении всего времени основной целью для коллектива предприятия являлось повышение качества продукции. Для чего предприятие постоянно осуществляет модернизацию оборудования, использует деревообрабатывающий инструмент и материалы ведущих производителей, производит обучение персонала. Поэтому двери ПМЦ можно реже увидеть на территории Беларуси, но зато практически во всех регионах России, в странах Балтии, в Казахстане, на Украине, в Азербайджане, а так же в Италии и в Дании, и даже в Ираке и Ливии.</p>
<p>В настоящее время на предприятии работают более 890 человек, производственные площади составляют порядка 10790 кв. м., годовой выпуск продукции &ndash; более 200 тыс. дверей. ПМЦ обладает значительными производственными мощностями на всех технологических этапах производства продукции. Имеется своя пилорама, сушильный комплекс, цеха по обработке древесины и лакокрасочный цех, цех по производству топливных гранул (пеллет).</p>
<p>Основная специализация предприятия &ndash; это производство дверей межкомнатных (глухих и остекленных) из клееного массива сосны, а также из ценных пород древесины, таких как ольха и дуб.</p>

